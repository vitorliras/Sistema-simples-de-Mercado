package Modelo;

public class ProdutosFrios  {
	

	private String TipoFrios;
	private String marcaFrios;
	
	
	
	public String getTipoFrios() {
		return TipoFrios;
	}
	public void setTipoFrios(String tipoFrios) {
		TipoFrios = tipoFrios;
	}
	public String getMarcaFrios() {
		return marcaFrios;
	}
	public void setMarcaFrios(String marcaFrios) {
		this.marcaFrios = marcaFrios;
	}
	
	
	
}
